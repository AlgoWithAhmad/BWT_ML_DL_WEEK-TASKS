
from math import add, subtract, multiply, divide, mod, exponent, sqrt

def main():
    print("Addition: 9 + 15 =", add(9, 15))
    
    print("Subtraction: 50 - 14 =", subtract(50, 14))
    
    print("Multiplication: 66 * 87 =", multiply(66, 87))
    
    print("Division: 15 / 5 =", divide(15, 5))
    
    print("Modulus: 150 % 6 =", mod(150, 6))
    
    print("Exponentiation: 3 ** 2 =", exponent(3, 2))
    
    print("Square root: sqrt(25) =", sqrt(25))

if __name__ == "__main__":
    main()
